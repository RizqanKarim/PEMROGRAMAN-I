a = 4
b = 8
c = 3

print(f"Variabel a bernilai {a}")
print(f"Variabel b bernilai {b}")
print(f"Variabel c bernilai {c}")

equal = 1 if a == b else 0
print(f"Apakah a sama dengan b ? jawabannya adalah {equal}")

morethan = 1 if b > c else 0
print(f"Apakah b lebih besar daripada c ? jawabannya adalah {morethan}")

unequal = 1 if a != c else 0
print(f"Apakah a tidak sama dengan c ? jawabannya adalah {unequal}")
